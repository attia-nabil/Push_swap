#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t	i;

	if (!n)
		return (0);
	i = 0;
	while (*((t_byte *)s1 + i) == *((t_byte *)s2 + i) && i + 1 < n)
		i++;
	return (*((t_byte *)s1 + i) - *((t_byte *)s2 + i));
}
