#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	e;

	e = ft_strlen(s);
	while (e >= 0)
	{
		if (s[e] == (char)c)
			return ((char *)(s + e));
		e--;
	}
	return (NULL);
}
