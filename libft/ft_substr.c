#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*rtn;

	if (!s)
		return (NULL);
	if (start >= ft_strlen(s))
		return (ft_strdup(""));
	if (len > ft_strlen(s) - start)
		len = ft_strlen(s) - start;
	rtn = (char *)malloc(sizeof(char) * len + 1);
	if (!rtn)
		return (NULL);
	ft_strlcpy(rtn, s + start, len + 1);
	return (rtn);
}
