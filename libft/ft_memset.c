#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;

	i = 0;
	while (i < len)
	{
		*((t_byte *)b + i) = ((t_byte)c & 0xff);
		i++;
	}
	return (b);
}
